-- 2. Create table
DROP TABLE IF EXISTS deposits;
CREATE TABLE deposits (
    id          SERIAL PRIMARY KEY,
    deposit_date    DATE          NOT NULL,
    weekday     VARCHAR(10)   NOT NULL,
    amount      NUMERIC(12,2) NOT NULL,
    acct_type   VARCHAR(20)   NOT NULL CHECK (acct_type IN ('Checking','Savings','IRA','CD')),
    opened_by   VARCHAR(20)   NOT NULL CHECK (opened_by IN ('Teller','New Accts','Online')),
    branch      VARCHAR(30)   NOT NULL,
    customer    VARCHAR(10)   NOT NULL CHECK (customer IN ('New','Existing'))
);

